#include "libft.h"

int		ft_atoi(const char *str)
{
	int neg;
	int res;
	int i;

	neg = 0;
	res = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	neg = i;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (str[neg] == '-')
		res = res * -1;
	return (res);
}
